@implementation TDServices

@synthesize logging, server, defaultServer;

@synthesize production;


#pragma mark Initialization

-(id)initWithServer:(NSString*)serverName{
	if(self = [self init]) {
		self.server = serverName;
	}
	return self;
}

+(TDServices*)service{
	return (TDServices*)[[TDServices alloc] init];
}

+(TDServices*)serviceWithServer:(NSString*)serverName{
	return (TDServices*)[[TDServices alloc] initWithServer:serverName];
}

#pragma mark Methods

-(void)setLogging:(BOOL)value{
	logging = value;
	[self updateServices];
}

-(void)setServer:(NSString*)value{
	server = value;
	[self updateServices];
}

-(void)updateServices{

	[self updateService: self.production];
}

-(void)updateService:(SoapService*)service{
	service.logging = self.logging;
	if(self.server == nil || self.server.length < 1) { return; }
	service.serviceUrl = [service.serviceUrl stringByReplacingOccurrencesOfString:defaultServer withString:self.server];
}

#pragma mark Getter Overrides


-(TDProduction*)production{
	if(production == nil) {
		production = [[TDProduction alloc] init];
	}
	return production;
}


